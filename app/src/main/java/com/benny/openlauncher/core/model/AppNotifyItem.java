package com.benny.openlauncher.core.model;

public class AppNotifyItem {
    private int number;
    private long time;

    public int getNumber() {
        return this.number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public long getTime() {
        return this.time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
